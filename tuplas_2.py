# Tuplas: las tuplas no son mutables o modificables son constantes

# Declarar una tupla
colores = ('Rojo', 'Azul', 'Verde', 'Amarillo')
print('\n', colores ,'\n')

# Acceder a un solo elemento de la tupla, indicando el indice
print(colores[1] ,'\n')

# Contar elementos de una tupla o lista
print(len(colores),'\n')
colores_lista = ['Rojo', 'Azul', 'Verde', 'Amarillo', 'Naranja']
print(len(colores_lista), '\n')

